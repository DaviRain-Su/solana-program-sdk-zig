    .os_tag = .solana,
    .cpu_features_add = std.Target.sbf.cpu.sbfv2.features,
};

// Link Solana program with proper script
pub fn linkSolanaProgram(b: *std.Build, lib: *std.Build.Step.Compile) void {
    const write_file_step = b.addWriteFiles();
    const linker_script = write_file_step.add("bpf.ld",
        \\PHDRS
        \\{
        \\text PT_LOAD  ;
        \\rodata PT_LOAD ;
        \\data PT_LOAD ;
        \\dynamic PT_DYNAMIC ;
        \\}
        \\
        \\SECTIONS
        \\{
        \\. = SIZEOF_HEADERS;
        \\.text : { *(.text*) } :text
        \\.rodata : { *(.rodata*) } :rodata
        \\.data.rel.ro : { *(.data.rel.ro*) } :rodata
        \\.dynamic : { *(.dynamic) } :dynamic
        \\.dynsym : { *(.dynsym) } :data
        \\.dynstr : { *(.dynstr) } :data
        \\.rel.dyn : { *(.rel.dyn) } :data
        \\/DISCARD/ : {
        \\*(.eh_frame*)
        \\*(.gnu.hash*)
        \\*(.hash*)
        \\}
        \\}
    );
    lib.setLinkerScript(linker_script);
    
    // Critical flags from joncinque's implementation
    lib.stack_size = 4096;  // Set stack size to BPF limit
    lib.link_z_notext = true;  // Allow code segment writes
    lib.root_module.pic = true;  // Position independent code
    lib.root_module.strip = true;  // Strip symbols
    
    // Original flags
    lib.linker_allow_shlib_undefined = true;
    lib.entry = .{ .symbol_name = "entrypoint" };
    lib.rdynamic = true;
}
